    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter The USN");
            jTextField1.grabFocus();
            return;
        }
        if (jPasswordField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter The Password");
            jPasswordField1.grabFocus();
            return;
        }
        String url = "jdbc:mysql://localhost:3306/library_management?useSSL=false";
        String user = "root";
        String password = "2GI18CS001";
        String sql = "select `USN`,`PASSWORD` From `student` WHERE `USN`=? AND `PASSWORD`=?";
        try (Connection con = DriverManager.getConnection(url, user, password);
                PreparedStatement pst = con.prepareStatement(sql)) {
            pst.setString(1, jTextField1.getText());
            pst.setString(2, String.valueOf(jPasswordField1.getPassword()));
            ResultSet r = pst.executeQuery();
            if (r.next()) {
                JOptionPane.showMessageDialog(this, "LOGGED IN SUCCESSFULLY");
                String msg=jTextField1.getText();
                Sstudent_p1 p = new Sstudent_p1(msg);
                p.setVisible(true);
                this.setVisible(false);
                return;
            }
            else{
             JOptionPane.showMessageDialog(this, "Invalid Details");   
             return;
            }

        } catch (SQLException ex) {

            System.out.println(ex.getMessage());
            System.err.println("ERROR");

        }
    }//GEN-LAST:event_jButton1ActionPerformed

